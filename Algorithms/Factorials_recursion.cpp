/* The user enters a value and the program calculates the factorial of the value.*/

#include <iostream>
using namespace std;

int factorial(int n)
{
if(n == 1) // stopping condition
return 1;
else
// recursive call
return factorial(n - 1) * n;
}

int main()
{
	int a = 0;
	cin >> a;
	cout << factorial(a); 
	return 0;
}
